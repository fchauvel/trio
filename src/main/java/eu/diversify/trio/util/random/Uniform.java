package eu.diversify.trio.util.random;

/**
 * The uniform distribution over a given interval
 */
public class Uniform extends Distribution {

    private final double min;
    private final double max;

    public Uniform(double min, double max) {
        super();

        assert min <= max:
                String.format("Invalid interval [%.1f, %.1f]! Lower bound is greater than the upper bound", min, max);

        this.min = min;
        this.max = max;
    }

    public double sample() {
        return min + generate().nextDouble() * (max - min);
    }

}
