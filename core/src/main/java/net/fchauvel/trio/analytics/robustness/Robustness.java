package net.fchauvel.trio.analytics.robustness;

/**
 * Gather descriptive statistics about the robustness of a given scenario
 */
public class Robustness {

    private final int scenarioId;
    private double total;
    private double minimum;
    private double maximum;
    private int count;

    public Robustness(int scenarioId) {
        this.scenarioId = scenarioId;
        this.total = 0;
        this.count = 0;
        this.minimum = Double.MAX_VALUE;
    }

    /**
     * @return the unique ID of the scenario to which this robustness is
     * associated
     */
    public int scenarioId() {
        return this.scenarioId;
    }

    /**
     * @return the average robustness
     */
    public double average() {
        checkState();
        return (double) total / count;
    }

    private void checkState() throws IllegalStateException {
        if (count == 0) {
            throw new IllegalStateException("No robustness recorded yet.");
        }
    }

    /**
     * @return the number of robustness computed
     */
    public int count() {
        return count;
    }

    /**
     * @return the minimum robustness measured in the scenario
     */
    public double minimum() {
        checkState();
        return minimum;
    }

    /**
     * @return the maximum robustness measured in the scenario
     */
    public double maximum() {
        checkState();
        return maximum;
    }

    /**
     * Record a new robustness in this sample
     * @param robustness the new robustness to be recorded
     */
    public void record(double robustness) {
        if (robustness < 0D || robustness > 1D) {
            final String description = String.format("Normalized robustness must be within [0, 1]  (found %.4f)", robustness);
            throw new IllegalArgumentException(description);
        }
        this.total += robustness;
        this.count += 1;
        if (robustness < minimum) {
            minimum = robustness;
        }
        if (robustness > maximum) {
            maximum = robustness;
        }
    }
    
    
    @Override
    public String toString() {
        return String.format("%.4f on %d sequence(s) [%.4f, %.4f]", average(), count(), minimum(), maximum());
    }

}
