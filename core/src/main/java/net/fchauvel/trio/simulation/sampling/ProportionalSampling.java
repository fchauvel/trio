package net.fchauvel.trio.simulation.sampling;

import net.fchauvel.trio.simulation.Topology;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 * Choose in proportion to the MTTF of each component
 */
public class ProportionalSampling extends Sampler {

    private final Map<String, Double> failureRates;

    public ProportionalSampling(Topology topology) {
        super(topology);
        this.failureRates = new HashMap<String, Double>();
        computeFailureRates();
    }

    private void computeFailureRates() {
        for (String each : topology.architecture().getComponentNames()) {
            final double failureRate = 1D / topology.architecture().getComponent(each).meanTimeToFailure();
            failureRates.put(each, failureRate);
        }
    }

    @Override
    public String pick() {
        final List<String> candidates = topology.activeComponents();
        final double draw = randomValue() * totalFailureRateFor(candidates);
        double accumulator = 0D;
        for (String eachCandidate: candidates) {
            accumulator += failureRates.get(eachCandidate);
            if (draw < accumulator) {
                return eachCandidate;
            }
        }
        return lastOf(candidates);
    }

    private String lastOf(List<String> candidates) {
        return candidates.get(candidates.size() - 1);
    }

    private double randomValue() {
        return random().nextDouble();
    }

    private double totalFailureRateFor(List<String> candidates) {
        double sum = 0D;
        for (String each: candidates) {
            sum += failureRates.get(each);
        }
        return sum;
    }

}
