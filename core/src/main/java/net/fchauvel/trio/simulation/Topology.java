package net.fchauvel.trio.simulation;

import net.fchauvel.trio.core.Assembly;
import net.fchauvel.trio.simulation.filter.Filter;
import java.util.List;

/**
 * A topology is the state of all component in a given assembly.
 */
public interface Topology {

    /**
     * @return the architecture associated with this topology
     */
    Assembly architecture();

    /**
     * @return the number of component in the topology
     */
    int size();

    /**
     * @return true if the topology contains a component with the given name
     * @param componentName the name of component of interest
     */
    boolean contains(String componentName);

    /**
     * Activate the component whose name is given
     *
     * @param component the name of the component to activate
     */
    void activate(String component);

    /**
     * Deactivate the component whose name is given
     *
     * @param component the name of component to deactivate
     */
    void inactivate(String component);

    /**
     * @param componentName the name of component of interest
     * @return true if the given component is active, false otherwise
     */
    boolean isActive(String componentName);

    /**
     * @param componentName the name of the component of interest
     * @return true if the given component is inactive, false otherwise
     */
    boolean isInactive(String componentName);

    /**
     * @return true if there exists at least one single active component
     */
    boolean hasActiveComponents();

    /**
     * @return the names of component that are active
     */
    List<String> activeComponents();

    /**
     * Create a view of some specific component, match the given selection
     * criterion
     *
     * @param selection the selection criterion
     * @return a restricted view on this topology
     */
    Topology select(Filter selection);

}
