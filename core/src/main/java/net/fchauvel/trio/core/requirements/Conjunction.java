package net.fchauvel.trio.core.requirements;

import net.fchauvel.trio.core.AssemblyVisitor;
import net.fchauvel.trio.simulation.AssemblyState;
import java.util.List;

/**
 * Logical conjunction between two requirements
 */
public final class Conjunction extends BinaryOperator {

    public Conjunction(Requirement... operands) {
        super(LogicalOperator.AND, operands);
    }

    public Conjunction(List<Requirement> operands) {
        super(LogicalOperator.AND, operands);
    }

    public void begin(AssemblyVisitor visitor) {
        visitor.enter(this);
    }

    public void end(AssemblyVisitor visitor) {
        visitor.exit(this);
    }

    public boolean isSatisfiedBy(AssemblyState topology) {
        for (Requirement anyOperand : getOperands()) {
            if (!anyOperand.isSatisfiedBy(topology)) {
                return false;
            }
        }
        return true;
    }

    private String formatted;

    @Override
    public String toString() {
        if (formatted == null) {
            StringBuilder buffer = new StringBuilder();
            buffer.append('(');
            for (int index = 0; index < getOperands().size(); index++) {
                final Requirement eachOperand = getOperands().get(index);
                buffer.append(eachOperand.toString());
                if (index < getOperands().size() - 1) {
                    buffer.append(AND_TEXT);
                }
            }
            buffer.append(')');
            formatted = buffer.toString();

        }
        return formatted;
    }
    private static final String AND_TEXT = " and ";

}
