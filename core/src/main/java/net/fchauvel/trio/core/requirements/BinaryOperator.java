package net.fchauvel.trio.core.requirements;

import net.fchauvel.trio.core.AssemblyPart;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collection;
import java.util.List;
import java.util.Objects;

/**
 *
 */
public abstract class BinaryOperator extends Requirement {

    private final LogicalOperator operator;
    private final List<Requirement> operands;

    
    
    public BinaryOperator(LogicalOperator operator, Requirement... operands) {
        this(operator, Arrays.asList(operands));
    }
    
    public BinaryOperator(LogicalOperator operator, List<Requirement> operands) {
        validateOperator(operator);
        validateOperands(operands);

        this.operator = operator;
        this.operands = new ArrayList<Requirement>(operands);    
         
    }
    

    private void validateOperands(List<Requirement> operands) throws IllegalArgumentException {
        if (operands.size() < 2) {
            throw new IllegalArgumentException("Invalid number of operands (expected at least 2, but found " + operands.size() + ")");
        }
        for (Requirement anyOperand : operands) {
            if (anyOperand == null) {
                throw new IllegalArgumentException("Invalid operand (found 'null')");
            }
        }
    }

    private void validateOperator(LogicalOperator operator) throws IllegalArgumentException {
        if (operator == null) {
            throw new IllegalArgumentException("Invalid operator ('null' found)");
        }
    }

    public List<Requirement> getOperands() {
        return this.operands;
    }

    public final Collection<AssemblyPart> subParts() {
        return new ArrayList<AssemblyPart>(operands);
    }

    private int hash = 0;

    @Override
    public final int hashCode() {
        if (hash == 0) {
            hash = Objects.hash(operator, operands.toArray());
        }
        return hash;
    }

    @Override
    public final boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final BinaryOperator other = (BinaryOperator) obj;
        if (this.operator != other.operator) {
            return false;
        }
        if (!operands.equals(other.operands)) {
            return false;
        }
        return true;
    }

}
