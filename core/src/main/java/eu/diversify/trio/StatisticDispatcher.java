package eu.diversify.trio;

import eu.diversify.trio.analytics.events.Statistic;
import eu.diversify.trio.analytics.events.StatisticListener;
import eu.diversify.trio.analytics.robustness.FailureSequence;
import eu.diversify.trio.analytics.robustness.Robustness;
import eu.diversify.trio.analytics.sensitivity.Sensitivity;
import eu.diversify.trio.analytics.threats.Threat;
import java.util.ArrayList;
import java.util.List;

/**
 * A publish-subscribe communication channel
 */
public class StatisticDispatcher implements StatisticListener {

    private final List<StatisticListener> listeners;

    public StatisticDispatcher() {
        listeners = new ArrayList<StatisticListener>();
    }

    /**
     * Register a new listener, which will be notify on any events received by
     * this dispatcher.
     *
     * @param listener the listener to notify
     */
    public void register(StatisticListener listener) {
        this.listeners.add(checkListener(listener));
    }

    private StatisticListener checkListener(StatisticListener listener) {
        if (listener == null) {
            throw new NullPointerException("Invalid listener ('null' found)");
        }
        return listener;
    }

    public void onFailureSequence(Statistic context, FailureSequence sequence) {
        for (StatisticListener each : listeners) {
            each.onFailureSequence(context, sequence);
        }
    }

    public void onRobustness(Statistic context, Robustness indicator) {
        for (StatisticListener each : listeners) {
            each.onRobustness(context, indicator);
        }
    }

    public void onSensitivityRanking(Statistic context, List<Sensitivity> indicator) {
        for (StatisticListener each : listeners) {
            each.onSensitivityRanking(context, indicator);
        }
    }

    public void onThreatRanking(Statistic context, List<Threat> indicator) {
        for (StatisticListener each : listeners) {
            each.onThreatRanking(context, indicator);
        }
    }

}
