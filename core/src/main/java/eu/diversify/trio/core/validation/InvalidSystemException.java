package eu.diversify.trio.core.validation;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;

/**
 * Raised when a system is invalid, i.e., when it contains some inconsistencies
 */
public class InvalidSystemException extends Exception {

    private static final long serialVersionUID = 1L;

    private final ArrayList<Inconsistency> errors;

    public InvalidSystemException(List<Inconsistency> errors) {
        this.errors = new ArrayList<Inconsistency>(errors);
    }
        
    public List<Inconsistency> getErrors() {
        return Collections.unmodifiableList(errors);
    }
    
    @Override
    public String toString() {
        final StringBuilder buffer = new StringBuilder(CAPACITY);
         
        buffer.append("Invalid model: ").append(System.lineSeparator());
        for(Inconsistency each: getErrors()) {
            buffer.append(" - ").append(each).append(System.lineSeparator());
        }
        
        return buffer.toString();
    }
    
    public static final int CAPACITY = 200;

}