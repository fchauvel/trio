package eu.diversify.trio.core;

import eu.diversify.trio.core.requirements.*;

/**
 * Ability to traverse a system, following the containment relationships.
 */
public abstract class DefaultAssemblyVisitor implements AssemblyVisitor {

    protected void defaultVisit() {
        // Nothing done, by default
    }

    @Override
    public void enter(Assembly system) {
        defaultVisit();
    }

    @Override
    public void exit(Assembly system) {
        defaultVisit();
    }

    @Override
    public void enter(Component component) {
        defaultVisit();
    }

    @Override
    public void exit(Component component) {
        defaultVisit();
    }

    @Override
    public void enter(Tag tag) {
        defaultVisit();
    }

    @Override
    public void exit(Tag tag) {
        defaultVisit();
    }

    @Override
    public void enter(Conjunction conjunction) {
        defaultVisit();
    }

    @Override
    public void exit(Conjunction conjunction) {
        defaultVisit();
    }

    @Override
    public void enter(Disjunction disjunction) {
        defaultVisit();
    }

    @Override
    public void exit(Disjunction disjunction) {
        defaultVisit();
    }

    @Override
    public void enter(Negation negation) {
        defaultVisit();
    }

    @Override
    public void exit(Negation negation) {
        defaultVisit();
    }

    @Override
    public void enter(Require require) {
        defaultVisit();
    }

    @Override
    public void exit(Require require) {
        defaultVisit();
    }

    @Override
    public void enter(Nothing nothing) {
        defaultVisit();
    }

    @Override
    public void exit(Nothing nothing) {
        defaultVisit();
    }

}
