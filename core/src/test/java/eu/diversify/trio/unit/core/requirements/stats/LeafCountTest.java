package eu.diversify.trio.unit.core.requirements.stats;

import net.fchauvel.trio.core.requirements.Requirement;
import net.fchauvel.trio.core.requirements.stats.LeafCount;
import java.util.ArrayList;
import java.util.Collection;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.junit.runners.Parameterized;

import static net.fchauvel.trio.core.Evaluation.evaluate;
import static net.fchauvel.trio.core.requirements.Factory.*;


import static org.hamcrest.MatcherAssert.*;
import static org.hamcrest.Matchers.*;

/**
 * Specifications of the leafCount operation
 */
@RunWith(Parameterized.class)
public class LeafCountTest {

    private final String name;
    private final Requirement requirement;
    private final int expectation;

    public LeafCountTest(String testName, Requirement requirement, int expectation) {
        this.name = testName;
        this.requirement = requirement;
        this.expectation = expectation;
    }
    
    @Parameterized.Parameters(name = "{0}")
    public static Collection<Object[]> examples() {
        final ArrayList<Object[]> examples = new ArrayList<Object[]>(10);
        
        examples.add(new Object[]{"require", require("X"), 1});
        examples.add(new Object[]{"nothing", nothing(), 1});
        examples.add(new Object[]{"and", require("X").and(require("Y")), 2});
        examples.add(new Object[]{"or", require("X").or(require("Y")), 2});
        examples.add(new Object[]{"not", not(require("X")), 1});
        
        return examples;
    }
    
    @Test
    public void shouldCountLeavesCorrectly() {
        final LeafCount leafCount = new LeafCount();        
   
        evaluate(leafCount).on(requirement);
   
        assertThat(leafCount.get(), is(equalTo(expectation)));
    }
   
    
}
